    private void jbFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbFiltrarActionPerformed

        try {
            
            HashMap parametroDt = new HashMap<>();
            HashMap parametroDtFim = new HashMap<>();

            parametroDt.put("dtInicio", new SimpleDateFormat("dd/MM/yyyy").parse(jtfDe.getText()));
            parametroDt.put("dtFim", new SimpleDateFormat("dd/MM/yyyy").parse(jtfAte.getText()));

            OperacaoBanco.OperacaoBancoMovimento ob = null;

            ob = new OperacaoBancoMovimento();
            ob.obterConexao();

            String src = "Jasper/movimentoPorData.jasper";
            JasperPrint jasperPrint = null;

            jasperPrint = JasperFillManager.fillReport(src, parametroDt, ob.obterConexao());

            JasperViewer view = new JasperViewer(jasperPrint, false);
            view.setVisible(true);
            ob.fechaConexao();
            dispose();
            
            AUXILIAR.AuxiliarLog log = new AuxiliarLog();
            log.log(transferedados.getText(), "MOVIMENTO", "RELATÓRIOS", Long.parseLong("611"));

        } catch (IOException ex) {
            Logger.getLogger(TelaMovimento.class.getName()).log(Level.SEVERE, null, ex);
        } catch (BiffException ex) {
            Logger.getLogger(TelaMovimento.class.getName()).log(Level.SEVERE, null, ex);
        } catch (JRException ex) {
            System.out.println("ERRO: " + ex.getMessage());
        } catch (ParseException ex) {
            Logger.getLogger(TelaMovimento.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jbFiltrarActionPerformed

