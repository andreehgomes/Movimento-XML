    public void salvar() {
        if (flag == 1) {
            int i = 0;

            while (i == 0) {
                if (jtfNome.getText().equals("") || jtfLogin.getText().equals("") || jtfSenha.getText().equals("")) {
                    i = 0;
                    jtfNome.requestFocus();
                    break;
                } else {
                    i = 1;

                    try {
                        OperacaoBanco.OperacaoBancoUsuarios obu = new OperacaoBancoUsuarios();
                        MODEL.ModelUsuarios usuario = new ModelUsuarios();

                        obu.obterConexao();
                        usuario.setLogin(jtfLogin.getText().toUpperCase());
                        usuario.setNome(jtfNome.getText().toUpperCase());
                        usuario.setSenha(jtfSenha.getText().toUpperCase());

                        obu.incluirUsuarios(usuario);
                        obu.fechaConexao();

                        jbNovo.setEnabled(true);
                        jbSalvar.setEnabled(false);
                        jmSalvar.setEnabled(false);
                        jbEditar.setEnabled(false);
                        jmEditar.setEnabled(false);
                        jtfLogin.setText("");
                        jtfNome.setText("");
                        jtfSenha.setText("");
                        jtfCodigo.setText("");
                        jtfLogin.setEnabled(false);
                        jtfNome.setEnabled(false);
                        jtfSenha.setEnabled(false);

                        preencherTabela("SELECT * FROM usuarios");

                    } catch (IOException ex) {
                        Logger.getLogger(TelaUsuarios.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (BiffException ex) {
                        Logger.getLogger(TelaUsuarios.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (Exception ex) {
                        Logger.getLogger(TelaUsuarios.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }

            try {
                AUXILIAR.AuxiliarLog aux = new AuxiliarLog();
                jtUsuario.changeSelection(jtUsuario.getRowCount() - 1, jtUsuario.getRowCount(), false, false);
                aux.log(recebedados.getText(), "USUARIOS", "INCLUIR", Long.parseLong(jtUsuario.getValueAt(jtUsuario.getSelectedRow(), 0).toString()));
            } catch (IOException ex) {
                Logger.getLogger(TelaUsuarios.class.getName()).log(Level.SEVERE, null, ex);
            } catch (BiffException ex) {
                Logger.getLogger(TelaUsuarios.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {

            int i = 0;

            while (i == 0) {
                if (jtfNome.getText().equals("") || jtfLogin.getText().equals("") || jtfSenha.getText().equals("")) {
                    i = 0;
                    jtfNome.requestFocus();
                    break;
                } else {
                    i = 1;

                    try {
                        OperacaoBanco.OperacaoBancoUsuarios obu = new OperacaoBancoUsuarios();
                        MODEL.ModelUsuarios usuario = new ModelUsuarios();

                        obu.obterConexao();
                        usuario.setCodusu(Integer.parseInt(jtfCodigo.getText()));
                        usuario.setLogin(jtfLogin.getText().toUpperCase());
                        usuario.setNome(jtfNome.getText().toUpperCase());
                        usuario.setSenha(jtfSenha.getText().toUpperCase());

                        obu.alterarUsuarios(usuario);
                        obu.fechaConexao();

                        preencherTabela("SELECT * FROM usuarios");

                    } catch (IOException ex) {
                        Logger.getLogger(TelaUsuarios.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (BiffException ex) {
                        Logger.getLogger(TelaUsuarios.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (Exception ex) {
                        Logger.getLogger(TelaUsuarios.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
            AUXILIAR.AuxiliarLog aux = new AuxiliarLog();
            try {
                aux.log(recebedados.getText(), "USUARIOS", "ALTERAR", Long.parseLong(jtfCodigo.getText().toString()));
            } catch (IOException ex) {
                Logger.getLogger(TelaUsuarios.class.getName()).log(Level.SEVERE, null, ex);
            } catch (BiffException ex) {
                Logger.getLogger(TelaUsuarios.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        jbNovo.setEnabled(true);
        jbSalvar.setEnabled(false);
        jmSalvar.setEnabled(false);
        jbEditar.setEnabled(false);
        jmEditar.setEnabled(false);
        jtfLogin.setText("");
        jtfNome.setText("");
        jtfSenha.setText("");
        jtfCodigo.setText("");
        jtfLogin.setEnabled(false);
        jtfNome.setEnabled(false);
        jtfSenha.setEnabled(false);
        flag = 0;
    }

