    public void preencherTabela(String sql) throws IOException, BiffException {
        OperacaoBancoCfop obc = new OperacaoBancoCfop();
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"COD", "LOGIN", "NOME"};
        obc.obterConexao();
        obc.executaSql(sql);

        try {
            obc.rs.first();

            do {
                dados.add(new Object[]{
                    obc.rs.getInt("codusu"),
                    obc.rs.getString("login"),
                    obc.rs.getString("nome"),});
            } while (obc.rs.next());
        } catch (SQLException ex) {
            //JOptionPane.showMessageDialog(rootPane, "Nenhum Usuário cadastrado");
        }

        ModelTabela modelo = new ModelTabela(dados, colunas);
        DefaultTableCellRenderer esquerda = new DefaultTableCellRenderer();
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        DefaultTableCellRenderer direita = new DefaultTableCellRenderer();
        direita.setHorizontalAlignment(SwingConstants.RIGHT);
        esquerda.setHorizontalAlignment(SwingConstants.LEFT);
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);

        jtUsuario.setModel((TableModel) modelo);
        jtUsuario.getColumnModel().getColumn(0).setPreferredWidth(90);
        jtUsuario.getColumnModel().getColumn(0).setCellRenderer(centralizado);
        jtUsuario.getColumnModel().getColumn(0).setResizable(true);
        jtUsuario.getColumnModel().getColumn(1).setPreferredWidth(150);
        jtUsuario.getColumnModel().getColumn(1).setCellRenderer(esquerda);
        jtUsuario.getColumnModel().getColumn(1).setResizable(true);
        jtUsuario.getColumnModel().getColumn(2).setPreferredWidth(200);
        jtUsuario.getColumnModel().getColumn(2).setResizable(true);

        jtUsuario.getTableHeader().setReorderingAllowed(true);
        jtUsuario.setAutoResizeMode(jtUsuario.AUTO_RESIZE_OFF);
        jtUsuario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        obc.fechaConexao();
    }


