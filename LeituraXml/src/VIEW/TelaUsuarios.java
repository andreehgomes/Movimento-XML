    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed

        AUXILIAR.AuxiliarControleAcesso aux = new AuxiliarControleAcesso();
        try {
            if (recebedados.getText().equals("ADM") || aux.controle(this.codigousuario, EMITIR_RELATORIOS) == true) {

                OperacaoBancoUsuarios ob = new OperacaoBancoUsuarios();
                ob.obterConexao();

                String src = "Jasper/relatorioListaUsuarios.jasper";
                JasperPrint jasperPrint = null;
                try {
                    jasperPrint = JasperFillManager.fillReport(src, null, ob.obterConexao());
                } catch (JRException ex) {
                    System.out.println("ERRO: " + ex.getMessage());
                }

                JasperViewer view = new JasperViewer(jasperPrint, false);
                view.setVisible(true);
                ob.fechaConexao();

            }
        } catch (IOException ex) {
            Logger.getLogger(TelaUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        } catch (BiffException ex) {
            Logger.getLogger(TelaUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TelaUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jMenuItem1ActionPerformed

