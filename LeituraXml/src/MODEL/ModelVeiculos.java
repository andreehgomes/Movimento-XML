
package MODEL;

public class ModelVeiculos {
    private int codveiculo;
    private String placa;
    private String modelo;
    private String marca;
    private int ano;
    private float mediacombustivel;

    public int getCodveiculo() {
        return codveiculo;
    }

    public void setCodveiculo(int codveiculo) {
        this.codveiculo = codveiculo;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }

    public float getMediacombustivel() {
        return mediacombustivel;
    }

    public void setMediacombustivel(float mediacombustivel) {
        this.mediacombustivel = mediacombustivel;
    }
    
    
    
}
